using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using Application.Business.Erp.SupplyChain.Base.Domain;


namespace Application.Business.Erp.SupplyChain.FinanceMng.Domain
{
    /// <summary>
    /// 当期收益结算单明细
    /// </summary>
    [Serializable]
    public class IncomeSettlementDetail : BaseDetail
    {
        private string incomeCategories;
        private string incomeProject;

        /// <summary>
        /// 收益类别
        /// </summary>
        virtual public string IncomeCategories
        {
            get { return incomeCategories; }
            set { incomeCategories = value; }
        }

        /// <summary>
        /// 收益项目
        /// </summary>
        virtual public string IncomeProject
        {
            get { return incomeProject; }
            set { incomeProject = value; }
        }

    }
}
