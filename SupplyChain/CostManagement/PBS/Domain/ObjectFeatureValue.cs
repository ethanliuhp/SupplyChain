using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using Application.Resource.MaterialResource.Domain;

namespace Application.Business.Erp.SupplyChain.CostManagement.PBS.Domain
{
    public class ObjectFeatureValue
    {
        private string id;
        private StandardUnit unit;
        private string description;
        private FeatureSet theFeatureSet;
        private string propertyId;
        private string propertyValue;
        /// <summary>
        /// id
        /// </summary>
        public virtual string Id
        {
            get { return id; }
            set { id = value; }
        }
        
        /// <summary>
        /// 计量单位
        /// </summary>
        public virtual StandardUnit Unit
        {
            get { return unit; }
            set { unit = value; }
        }
        
        /// <summary>
        /// 说明
        /// </summary>
        public virtual string Description
        {
            get { return description; }
            set { description = value; }
        }
        
        /// <summary>
        /// 所属特性集
        /// </summary>
        public virtual FeatureSet TheFeatureSet
        {
            get { return theFeatureSet; }
            set { theFeatureSet = value; }
        }

        
        /// <summary>
        /// 属性Id
        /// </summary>
        public virtual string PropertyId
        {
            get { return propertyId; }
            set { propertyId = value; }
        }
       
        /// <summary>
        /// 属性值
        /// </summary>
        public virtual string PropertyValue
        {
            get { return propertyValue; }
            set { propertyValue = value; }
        }
    }
}
