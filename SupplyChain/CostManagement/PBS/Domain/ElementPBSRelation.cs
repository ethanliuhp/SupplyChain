using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace Application.Business.Erp.SupplyChain.CostManagement.PBS.Domain
{
    /// <summary>
    /// 元素所属关系
    /// </summary>
    [Serializable]
    public class ElementPBSRelation
    {
        private string id;
        private long version;
        private string pbsId;
        private string pbsName;
        private string relationType;
        private string elementId;
        private string elementName;

        /// <summary>
        /// 版本
        /// </summary>
        public virtual long Version
        {
            get { return version; }
            set { version = value; }
        }
        /// <summary>
        /// 
        /// </summary>
        public virtual string Id
        {
            get { return id; }
            set { id = value; }
        }

        /// <summary>
        /// pbs节点Id
        /// </summary>
        public virtual string PbsId
        {
            get { return pbsId; }
            set { pbsId = value; }
        }

        /// <summary>
        /// pbs节点名称
        /// </summary>
        public virtual string PbsName
        {
            get { return pbsName; }
            set { pbsName = value; }
        }
        /// <summary>
        /// 关系类型
        /// </summary>
        public virtual string RelationType
        {
            get { return relationType; }
            set { relationType = value; }
        }

        /// <summary>
        /// 元素Id
        /// </summary>
        public virtual string ElementId
        {
            get { return elementId; }
            set { elementId = value; }
        }

        /// <summary>
        /// 元素名称
        /// </summary>
        public virtual string ElementName
        {
            get { return elementName; }
            set { elementName = value; }
        }
    }
}
