using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using Application.Business.Erp.SupplyChain.Base.Domain;
using Application.Business.Erp.SupplyChain.CostManagement.WBS.Domain;

namespace Application.Business.Erp.SupplyChain.BasicData.UnitMng.Domain
{
    /// <summary>
    /// 计量单位主表
    /// </summary>
    [Serializable]
    public class UnitMaster : BaseMaster
    {
        private string billTypeName;
        private string billName;

        /// <summary>
        /// 单据类名
        /// </summary>
        virtual public string BillTypeName
        {
            get { return billTypeName; }
            set { billTypeName = value; }
        }
        /// <summary>
        /// 单据名称
        /// </summary>
        virtual public string BillName
        {
            get { return billName; }
            set { billName = value; }
        }
    }
}
