using System;
using System.Collections.Generic;
using System.Text;

namespace Application.Business.Erp.SupplyChain.BasicData.Domain
{
    /// <summary>
    /// 指标模块的基础数据值对象
    /// </summary>
    public class IndicatorBasicValue
    {
        private string code;
        private string name;

        /// <summary>
        /// 编码
        /// </summary>
        virtual public string Code
        {
            get { return code; }
            set { code = value; }
        }

        /// <summary>
        /// 名称
        /// </summary>
        virtual public string Name
        {
            get { return name; }
            set { name = value; }
        }
    }
}
