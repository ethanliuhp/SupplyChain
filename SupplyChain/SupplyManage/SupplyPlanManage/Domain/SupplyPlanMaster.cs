using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using Application.Business.Erp.SupplyChain.Base.Domain;
using Application.Business.Erp.SupplyChain.SupplyManage.Base.Domain;

namespace Application.Business.Erp.SupplyChain.SupplyManage.SupplyPlanManage.Domain
{
    /// <summary>
    /// 采购计划主表
    /// </summary>
    [Serializable]
    public class SupplyPlanMaster : BaseSupplyMaster
    {
        private string specialType;
        private string planName;

        /// <summary>
        /// 专业分类
        /// </summary>
        virtual public string SpecialType
        {
            get { return specialType; }
            set { specialType = value; }
        }

        /// <summary>
        /// 计划名称
        /// </summary>
        virtual public string PlanName
        {
            get { return planName; }
            set { planName = value; }
        }
    }
}
