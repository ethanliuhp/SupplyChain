using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using Application.Business.Erp.SupplyChain.Base.Domain;
using VirtualMachine.Patterns.BusinessEssence.Domain;

namespace Application.Business.Erp.SupplyChain.EngineerManage.DocApprovalManage.Domain
{
    [Serializable]
    public class DocApprovalMng:BaseMaster
{
    private string chName;                       //策划名称
    private string approvalStyle;                 //审批类型
    private string remark;                              //备注
 


   /// <summary>
    /// 策划名称
   /// </summary>
    virtual public string ChName
    {
        get { return chName; }
        set { chName = value; }
    }        
    /// <summary>
    /// 审批类型
    /// </summary>
    virtual public string ApprovalStyle
    {
        get { return approvalStyle; }
        set { approvalStyle = value; }
    }
    /// <summary>
    /// 备注
    /// </summary>
    virtual public string Remark
    {
        get { return remark; }
        set { remark = value; }
    }

     
    }
}
