namespace Application.Business.Erp.SupplyChain.Client.CostManagement.EngineerChangeMng
{
    public partial class VEngineerChangeSearchCon : BasicUserControl
    {
        private MEngineerChangeMng model = new MEngineerChangeMng();
        private VEngineerChangeSearchList searchList;
        public VEngineerChangeSearchCon(VEngineerChangeSearchList searchList)
        {
            this.searchList = searchList;
            InitializeComponent();
            this.ViewType = VirtualMachine.Component.WinMVC.core.ViewType.Search;
            this.InitForm();
            this.InitEvent();
            btnOK.Focus();
        }
        private void InitForm()
        {
            this.dtpDateBegin.Value = ConstObject.LoginDate.AddDays(-7);
            this.dtpDateEnd.Value = ConstObject.LoginDate;
        }

        private void InitEvent()
        {
            this.btnCancel.Click += new EventHandler(btnCancel_Click);
            this.customButton2.Click += new EventHandler(btnOK_Click);
            this.btnOK.Click += new EventHandler(btnOK_Click);
            this.txtCodeBegin.tbTextChanged += new EventHandler(txtCodeBegin_tbTextChanged);
        }
        void txtCodeBegin_tbTextChanged(object sender, EventArgs e)
        {
            this.txtCodeEnd.Text = this.txtCodeBegin.Text;
        }

        void btnOK_Click(object sender, EventArgs e)
        {
            ObjectQuery oq = new ObjectQuery();

            if (this.txtCodeBegin.Text != "")
            {
                oq.AddCriterion(Expression.Between("Code", this.txtCodeBegin.Text, this.txtCodeEnd.Text));
            }
            CurrentProjectInfo projectInfo = StaticMethod.GetProjectInfo();
            oq.AddCriterion(Expression.Like("CreatePersonName", ConstObject.LoginPersonInfo.Name));
            oq.AddCriterion(Expression.Like("ProjectName", projectInfo.Name));
            oq.AddCriterion(Expression.Ge("CreateDate", this.dtpDateBegin.Value.Date));
            oq.AddCriterion(Expression.Lt("CreateDate", this.dtpDateEnd.Value.AddDays(1).Date));
           
            oq.AddOrder(new Order("Code", false));
            IList list = model.EngineerChangeSrv.GetEngineerChange(oq);
            searchList.RefreshData(list);
            this.customButton2.FindForm().Close();
        }

        void btnCancel_Click(object sender, EventArgs e)
        {
            this.btnOK.FindForm().Close();
        }
    }
}
